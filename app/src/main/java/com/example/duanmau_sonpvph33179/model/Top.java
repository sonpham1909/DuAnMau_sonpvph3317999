package com.example.duanmau_sonpvph33179.model;

public class Top {

    private String ten;
    private int sl;

    public Top() {
    }

    public Top(String ten, int sl) {
        this.ten = ten;
        this.sl = sl;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getSl() {
        return sl;
    }

    public void setSl(int sl) {
        this.sl = sl;
    }
}
